class TestMultiLineImports:
    """Some packages have multi-line import statements (e.g., resizeobserver, structuredclone)."""

    def test_multiline_import_preserved(self, service):
        recs = service.get_recommendations(
            unsupported_features={'structuredclone'},
            partial_features=set(),
            browsers={'ie': '11'},
        )
        assert len(recs) == 1
        import_stmt = recs[0].packages[0].import_statement
        # structuredClone import has 2 lines
        assert '\n' in import_stmt or 'structuredClone' in import_stmt
