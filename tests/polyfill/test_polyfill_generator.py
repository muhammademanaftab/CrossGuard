class TestGenerateContent:
    def test_header_present(self, npm_recs):
        content = generate_polyfills_content(npm_recs)
        assert '/**' in content
        assert 'Auto-generated by Cross Guard' in content
        assert "import './polyfills';" in content

    def test_npm_imports_present(self, npm_recs):
        content = generate_polyfills_content(npm_recs)
        assert "import 'whatwg-fetch';" in content

    def test_feature_name_as_comment(self, npm_recs):
        content = generate_polyfills_content(npm_recs)
        assert '// Fetch API' in content
        assert '// JavaScript Promises' in content

    def test_fallback_not_in_content(self, fallback_recs):
        """Fallback-only recs produce no imports — just a 'No npm polyfills needed' note."""
        content = generate_polyfills_content(fallback_recs)
        assert 'import' not in content or "import './polyfills'" in content

    def test_empty_recs_still_valid(self, empty_recs):
        content = generate_polyfills_content(empty_recs)
        assert 'Auto-generated by Cross Guard' in content

    def test_content_is_string(self, npm_recs):
        content = generate_polyfills_content(npm_recs)
        assert isinstance(content, str)

    def test_content_has_timestamp(self, npm_recs):
        content = generate_polyfills_content(npm_recs)
        # Timestamp format: YYYY-MM-DD HH:MM:SS
        import re
        assert re.search(r'\d{4}-\d{2}-\d{2} \d{2}:\d{2}:\d{2}', content)


