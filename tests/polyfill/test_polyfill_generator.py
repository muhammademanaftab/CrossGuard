class TestSpecificPackageContent:
    """Verify that the generated content uses the correct import statements from polyfill_map."""

    @pytest.mark.parametrize("feature_id,expected_import", [
        ('fetch', "import 'whatwg-fetch';"),
        ('intersectionobserver', "import 'intersection-observer';"),
        ('requestidlecallback', "import 'requestidlecallback-polyfill';"),
        ('abortcontroller', "import 'abortcontroller-polyfill/dist/polyfill-patch-fetch';"),
        ('globalthis', "import 'core-js/features/global-this';"),
        ('classlist', "import 'classlist-polyfill';"),
    ])
    def test_import_statement_in_generated_content(self, service, feature_id, expected_import):
        recs = service.get_recommendations(
            unsupported_features={feature_id},
            partial_features=set(),
            browsers={'ie': '11'},
        )
        content = generate_polyfills_content(recs)
        assert expected_import in content, (
            f"Expected '{expected_import}' in generated content for {feature_id}"
        )

    @pytest.mark.parametrize("feature_id,expected_npm", [
        ('fetch', 'whatwg-fetch'),
        ('intersectionobserver', 'intersection-observer'),
        ('structuredclone', '@ungap/structured-clone'),
        ('broadcastchannel', 'broadcast-channel'),
        ('dialog', 'dialog-polyfill'),
    ])
    def test_npm_package_in_install_command(self, service, feature_id, expected_npm, tmp_path):
        recs = service.get_recommendations(
            unsupported_features={feature_id},
            partial_features=set(),
            browsers={'ie': '11'},
        )
        out = tmp_path / 'polyfills.js'
        generate_polyfills_file(recs, str(out))
        content = out.read_text(encoding='utf-8')
        assert expected_npm in content, (
            f"Expected '{expected_npm}' in file for {feature_id}"
        )


