class TestGenerateFile:
    def test_creates_file(self, tmp_path, npm_recs):
        out = tmp_path / 'polyfills.js'
        result = generate_polyfills_file(npm_recs, str(out))
        assert Path(result).exists()

    def test_returns_path_string(self, tmp_path, npm_recs):
        out = tmp_path / 'polyfills.js'
        result = generate_polyfills_file(npm_recs, str(out))
        assert result == str(out)

    def test_file_contains_imports(self, tmp_path, npm_recs):
        out = tmp_path / 'polyfills.js'
        generate_polyfills_file(npm_recs, str(out))
        content = out.read_text(encoding='utf-8')
        assert "import 'whatwg-fetch';" in content

    def test_file_has_install_command(self, tmp_path, npm_recs):
        out = tmp_path / 'polyfills.js'
        generate_polyfills_file(npm_recs, str(out))
        content = out.read_text(encoding='utf-8')
        assert 'npm install' in content

    def test_file_has_install_packages(self, tmp_path, npm_recs):
        out = tmp_path / 'polyfills.js'
        generate_polyfills_file(npm_recs, str(out))
        content = out.read_text(encoding='utf-8')
        assert 'whatwg-fetch' in content

    def test_file_encoding_utf8(self, tmp_path, npm_recs):
        out = tmp_path / 'polyfills.js'
        generate_polyfills_file(npm_recs, str(out))
        # Read as bytes and verify UTF-8
        raw = out.read_bytes()
        raw.decode('utf-8')  # should not raise

    def test_fallback_only_file(self, tmp_path, fallback_recs):
        """File should say 'No npm polyfills needed' when only CSS fallbacks."""
        out = tmp_path / 'polyfills.js'
        generate_polyfills_file(fallback_recs, str(out))
        content = out.read_text(encoding='utf-8')
        assert 'No npm polyfills needed' in content

    def test_mixed_file_has_imports_and_header(self, tmp_path, mixed_recs):
        out = tmp_path / 'polyfills.js'
        generate_polyfills_file(mixed_recs, str(out))
        content = out.read_text(encoding='utf-8')
        # Should contain npm imports but not fallback code
        assert "import 'whatwg-fetch';" in content or "dialog-polyfill" in content
        assert 'Auto-generated by Cross Guard' in content

    def test_empty_recs_file(self, tmp_path, empty_recs):
        out = tmp_path / 'polyfills.js'
        generate_polyfills_file(empty_recs, str(out))
        content = out.read_text(encoding='utf-8')
        assert 'No npm polyfills needed' in content

    def test_overwrite_existing_file(self, tmp_path, npm_recs):
        out = tmp_path / 'polyfills.js'
        out.write_text('OLD CONTENT', encoding='utf-8')
        generate_polyfills_file(npm_recs, str(out))
        content = out.read_text(encoding='utf-8')
        assert 'OLD CONTENT' not in content
        assert 'Auto-generated' in content

    def test_nested_directory_path(self, tmp_path, npm_recs):
        out = tmp_path / 'deep' / 'nested' / 'polyfills.js'
        out.parent.mkdir(parents=True)
        result = generate_polyfills_file(npm_recs, str(out))
        assert Path(result).exists()


